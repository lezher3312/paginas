    void mostrardatos(String Valor){//cambio Valor
        DefaultTableModel modelo=new DefaultTableModel();//importar 
        modelo.addColumn("Id");
        modelo.addColumn("Usuario");
        modelo.addColumn("Pass");
        tb.setModel(modelo);
        String sql="";//cambio y el if
        if (Valor.equals("")){
            sql = "SELECT * from usuarios";
        }
        else{
            sql = "SELECT * from usuarios WHERE id='"+Valor+"'";
        }
        
        String []datos=new String[3];
        
        try {
            Statement st=con.createStatement();
            ResultSet rs = st.executeQuery(sql);//cambio
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                
                //para las filas
                modelo.addRow(datos);
            }
            tb.setModel(modelo);
        }catch (Exception e){
            
        }  
       
    
        
    }
    
      public void modificar(){
        int fila = tb.getSelectedRow();
        if(fila>=0){
            txtId.setText(tb.getValueAt(fila,0).toString());
            txtUsuario.setText(tb.getValueAt(fila,1).toString());
            txtPass.setText(tb.getValueAt(fila,2).toString());
        }else{
             JOptionPane.showMessageDialog(null,"No seleciono fila");
         }
    }
    
